package de.rexlNico.Mafia.objects;

public class RGB {

	private int red;
	private int green;
	private int blue;
	
	public RGB(int red, int green, int blue){
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
	public int getBlue() {
		return blue;
	}
	public int getGreen() {
		return green;
	}
	public int getRed() {
		return red;
	}
	
}
